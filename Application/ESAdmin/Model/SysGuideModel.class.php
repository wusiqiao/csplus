<?php

namespace ESAdmin\Model;

use Common\Lib\Model\TreeDataModel;

class SysGuideModel extends TreeDataModel {
    /**
     *@var 是否显示 -- 显示
     * */
    const IS_SHOW_10 = 10;
    /**
     *@var 是否显示 -- 不显示
     * */
    const IS_SHOW_20 = 20;

    public function _before_write(&$data){

        if($data['parent_id'] > 0){
            !empty($data['url']) OR E('请输入教程链接');
        }

        $data['is_show'] = $data['is_show'] == self::IS_SHOW_10 ? self::IS_SHOW_10 : self::IS_SHOW_20;
        parent::_before_write($data); // TODO: Change the autogenerated stub
    }
}
